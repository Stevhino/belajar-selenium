public class ArithmeticOperations {
    public static void main(String[] args) {
        int i = 5;
        System.out.println("i saat ini adalah = " +i);
        i++;
        System.out.println("i setelah ditambahkan adalah = " + i);
        ++i;
        System.out.println("i setelah ditambahkan adalah = " + ++i);

        System.out.println("i setelah ditambahkan adalah = " + i++);

        System.out.println("i setelah ditambahkan adalah = " + i);
    }
}
